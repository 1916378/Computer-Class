public class SampleClass 
{
	int var1;
	int var2;
	String var3;
	
	public SampleClass(int uno, int dos, String tres)
	{
		var1 = uno;
		var2 = dos;
		var3 = tres;
	}
	
	public int getVar1()
	{
		return var1;
	}
	
	public int getVar2()
	{
		return var2;
	}
	
	public String getVar3()
	{
		return var3;
	}
	
	public void setVar1(int uno)
	{
		var1 = uno;
	}
	
	public void setVar2(int dos)
	{
		var2 = dos;
	}
	
	public void setVar3(String tres)
	{
	var3 = tres;
	}
}
	